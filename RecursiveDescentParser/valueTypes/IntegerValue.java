package valueTypes;
/**
 * The Value representing integers.
 * 
 * @author Curran Kelleher
 * 
 */
public class IntegerValue extends DecimalValue {

	/**
	 * Constructs an IntegerValue initialized with the specified double value.
	 * 
	 * @param value
	 *            the value this DecimalValue will have.
	 */
	public IntegerValue(int value) {
		super(value);
	}
}
